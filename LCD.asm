ORG 000H
LCALL COMMAND_WRITE
LCALL COMMAND_DATA
HERE:SJMP HERE
ORG 0020H
COMMAND_WRITE:
MOV DPTR,#COMMAND_WORD
REP1:MOV A,#00H
MOVC A,@A+DPTR
JZ L1
MOV P3,A
CLR P2.0
CLR P2.1
SETB P2.2
LCALL DELAY
CLR P2.2
INC DPTR 
SJMP REP1
L1:RET
ORG 0050H
MOV DPTR,#COMMAND_DATA
REP2:MOV A,#00H
MOVC A,@A+DPTR
JZ L2
MOV P3,A
SETB P2.0
CLR P2.1
SETB P2.2
LCALL DELAY
CLR P2.2
INC DPTR
SJMP REP2
L2:RET
ORG 0080H
COMMAND_WORD:DB 38H,0FH,81H,00H
ORG 0090H
COMMAND_DATA:DB 'E','C','E',00H
DELAY:
MOV R1,#250H
HERE2:MOV R2,#34H
HERE1:DJNZ R2, HERE1
DJNZ R1,HERE2
RET 
END